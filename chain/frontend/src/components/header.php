            function fetchNotifications() {
                $.ajax({
                    url: '/chain-fortune/action/fetch_notifications',
                    method: 'GET',
                    success: function (response) {
                        const result = response;

                        if (result.status === 'success') {
                            const notifications = result.notifications;
                            const notificationsList = $('#notificationList');

                            if (notifications.length > 0) {
                                let html = '';

                                notifications.forEach(notification => {
                                    html += `
                                        <div class="notification-item ${notification.is_read === 0 ? 'unread' : ''}" 
                                            data-id="${notification.notification_id}" 
                                            data-type="success"
                                            data-message="${notification.message}">
                                            <label class="custom-checkbox">
                                                <input type="checkbox" class="notification-checkbox">
                                                <span class="checkmark"></span>
                                            </label>
                                            <div class="notification-icon success">
                                                <img src='${notification.notification_symbol}' alt="Success">
                                            </div>
                                            <div class="notification-content">
                                                <div class="notification-message">${notification.message}</div>
                                                <div class="notification-date">
                                                    ${notification.created_at}
                                                </div>
                                                <div class="notification-actions">
                                                    <div class="notification-action view">View</div>
                                                    <div class="notification-action delete">Delete</div>
                                                </div>
                                            </div>
                                        </div>
                                    `;
                                });

                                notificationsList.html(html);
                                notificationSystem.bindDynamicEvents();
                            } else {
                                notificationsList.html('<div style="display: flex; justify-content: center;" id="no-notifications" class="notification-item"><p style="text-align:center;">No notifications!</p></div>');
                                notificationsList.removeAttr('style');
                            }

                            updateNotificationButtonsState();

                        } else {
                            console.error(result.message);
                        }
                    }
                });
            }


            function updateNotificationButtonsState() {
                const $notificationList = $('#notificationList');
                const $items = $notificationList.children('.notification-item');

                const hasOnlyNoNotificationItem =
                    $items.length === 1 &&
                    $items.first().attr('id') === 'no-notifications';

                if (hasOnlyNoNotificationItem) {
                    $('#deleteMarkedBtn, #markAllReadBtn, #deleteAllBtn')
                        .prop('disabled', true)
                        .css({
                            'background-color': '#666',
                            'color': '#fff',
                            'cursor': 'not-allowed'
                        });
                } else {
                    $('#deleteMarkedBtn, #markAllReadBtn, #deleteAllBtn')
                        .prop('disabled', false)
                        .css({
                            'background-color': '',
                            'color': '',
                            'cursor': ''
                        });
                }
            }


            fetchNotifications(); 
            updateNotificationButtonsState();
            setInterval(fetchNotifications, 900000);

            

